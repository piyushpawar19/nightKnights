from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Optional, Set, Type

from src.evaluation.latency_metrics import (
    ExportLatencyMetric,
    ExplanationLatencyMetric,
    LatencyMetrics,
    RankingLatencyMetric,
    RetrievalLatencyMetric,
    ThroughputMetric,
    TotalPipelineRuntimeMetric,
)
from src.evaluation.metrics import (
    ExplanationConfidenceDistributionMetric,
    ExplanationSuccessRateMetric,
    FallbackRateMetric,
)
from src.evaluation.ranking_metrics import (
    MAPMetric,
    MRRMetric,
    NDCGMetric,
    RankingPrecisionMetric,
    RankingRecallMetric,
    TopKAccuracyMetric,
)
from src.evaluation.report_generator import EvaluationReportGenerator
from src.evaluation.retrieval_metrics import (
    CoverageMetric,
    HitRateMetric,
    PrecisionMetric,
    RecallMetric,
)
from src.interfaces.evaluation_interface import Evaluator, Metric
from src.models.domain_models import Explanation, RankedCandidate, RetrievalResult
from src.schemas.graph_schema import NodeTimestamp
from src.utils.config_manager import ConfigManager
from src.utils.logger import get_logger

logger = get_logger(__name__)


class EvaluationAgent(Evaluator):
    """Orchestrates the calculation of various evaluation metrics and report generation."""

    def __init__(
        self,
        config_manager: ConfigManager,
        report_generator: EvaluationReportGenerator,
        # Dependency injection for metrics, allowing for flexible metric sets
        retrieval_metrics: Optional[List[Metric]] = None,
        ranking_metrics: Optional[List[Metric]] = None,
        explainability_metrics: Optional[List[Metric]] = None,
        latency_metrics: Optional[List[Metric]] = None,
    ) -> None:
        self.config_manager = config_manager
        self.report_generator = report_generator

        self.retrieval_metrics = retrieval_metrics or [
            RecallMetric(),
            PrecisionMetric(),
            HitRateMetric(),
            CoverageMetric(),
        ]
        self.ranking_metrics = ranking_metrics or [
            MRRMetric(),
            NDCGMetric(),
            MAPMetric(),
            RankingPrecisionMetric(),
            RankingRecallMetric(),
            TopKAccuracyMetric(),
        ]
        self.explainability_metrics = explainability_metrics or [
            ExplanationSuccessRateMetric(),
            FallbackRateMetric(),
            ExplanationConfidenceDistributionMetric(),
        ]
        self.latency_metrics = latency_metrics or [
            RetrievalLatencyMetric(),
            RankingLatencyMetric(),
            ExplanationLatencyMetric(),
            ExportLatencyMetric(),
            TotalPipelineRuntimeMetric(),
            ThroughputMetric(),
        ]

        self.all_metrics: Dict[str, List[Metric]] = {
            "retrieval": self.retrieval_metrics,
            "ranking": self.ranking_metrics,
            "explainability": self.explainability_metrics,
            "latency": self.latency_metrics,
        }

    def evaluate(
        self,
        ranked_candidates: List[RankedCandidate],
        ground_truth_relevant_ids: Dict[str, Set[str]], # Query ID -> Set of relevant candidate IDs
        retrieval_results_per_query: Dict[str, List[str]], # Query ID -> Ordered list of retrieved candidate IDs
        explanations: List[Explanation],
        timestamps: List[NodeTimestamp],
        num_initial_candidates: int, # Total candidates processed at start of pipeline
        k_values: Dict[str, int], # e.g., {"recall_k": 10, "ndcg_k": 10}
        report_name: str = "evaluation_report",
        report_format: str = "json",
        output_dir: Optional[Path] = None,
        pipeline_version: str = "1.0.0",
        summary: str = "",
    ) -> Dict[str, Any]:
        """Orchestrates the calculation of all metrics and generates a report.

        Args:
            ranked_candidates (List[RankedCandidate]): The ranked candidates from the pipeline.
            ground_truth_relevant_ids (Dict[str, Set[str]]): Ground truth for relevance.
            retrieval_results_per_query (Dict[str, List[str]]): Raw retrieval results before ranking.
            explanations (List[Explanation]): Explanations generated by the explanation service.
            timestamps (List[NodeTimestamp]): List of timestamps for pipeline nodes.
            num_initial_candidates (int): Total number of candidates processed.
            k_values (Dict[str, int]): Dictionary of K values for various metrics.
            report_name (str): Base name for the generated report file.
            report_format (str): Format for the report (json, csv, md).
            output_dir (Optional[Path]): Directory to save the report. Uses config if None.
            pipeline_version (str): Version of the pipeline being evaluated.
            summary (str): A summary description of this evaluation run.

        Returns:
            Dict[str, Any]: Aggregated evaluation results.
        """
        logger.info("Starting evaluation with report_name=\"%s\"", report_name)
        overall_results: Dict[str, Any] = {
            "timestamp": datetime.now(tz=timezone.utc).isoformat(),
            "pipeline_version": pipeline_version,
            "summary": summary,
        }

        output_dir = output_dir or self.config_manager.get_evaluation_config().output_dir

        # --- Retrieval Metrics ---
        retrieval_metrics_results: Dict[str, Any] = {}
        all_retrieved_ids = []
        all_possible_relevant_ids = set()

        for query_id, retrieved_ids_list in retrieval_results_per_query.items():
            all_retrieved_ids.append(set(retrieved_ids_list))
            all_possible_relevant_ids.update(ground_truth_relevant_ids.get(query_id, set()))

            for metric in self.retrieval_metrics:
                try:
                    if isinstance(metric, RecallMetric) or isinstance(metric, PrecisionMetric):
                        k = k_values.get(f"{metric.__class__.__name__.lower().replace('metric', '')}_k", 10)
                        retrieval_metrics_results.update(metric.calculate(retrieved_ids_list, ground_truth_relevant_ids.get(query_id, set()), k))
                    elif isinstance(metric, HitRateMetric):
                        retrieval_metrics_results.update(metric.calculate(retrieved_ids_list, ground_truth_relevant_ids.get(query_id, set())))
                    # Coverage is calculated once for all queries
                except Exception as e:
                    logger.error("Error calculating retrieval metric %s for query %s: %s", metric.__class__.__name__, query_id, e)

        for metric in self.retrieval_metrics:
            if isinstance(metric, CoverageMetric):
                try:
                    retrieval_metrics_results.update(metric.calculate(all_retrieved_ids, all_possible_relevant_ids))
                except Exception as e:
                    logger.error("Error calculating global retrieval metric %s: %s", metric.__class__.__name__, e)

        overall_results["retrieval_metrics"] = retrieval_metrics_results

        # --- Ranking Metrics ---
        ranking_metrics_results: Dict[str, Any] = {}
        ranked_candidate_ids = [c.candidate_id for c in ranked_candidates]

        # Assuming a single query context for ranking ground truth for simplicity here
        # In a multi-query scenario, this would loop over queries.
        if ground_truth_relevant_ids:
            # Take the first query's relevant IDs as representative for ranking evaluation
            # This needs to be adapted for true multi-query ranking evaluation.
            representative_relevant_ids = next(iter(ground_truth_relevant_ids.values()))
        else:
            representative_relevant_ids = set()


        for metric in self.ranking_metrics:
            try:
                if isinstance(metric, (NDCGMetric, RankingPrecisionMetric, RankingRecallMetric, TopKAccuracyMetric)):
                    k = k_values.get(f"{metric.__class__.__name__.lower().replace('metric', '').replace('ranking', '')}_k", 10) # e.g. ndcg_k
                    ranking_metrics_results.update(metric.calculate(ranked_candidate_ids, representative_relevant_ids, k))
                elif isinstance(metric, (MRRMetric, MAPMetric)):
                    ranking_metrics_results.update(metric.calculate(ranked_candidate_ids, representative_relevant_ids))
            except Exception as e:
                logger.error("Error calculating ranking metric %s: %s", metric.__class__.__name__, e)
        overall_results["ranking_metrics"] = ranking_metrics_results

        # --- Explainability Metrics ---
        explainability_metrics_results: Dict[str, Any] = {}
        for metric in self.explainability_metrics:
            try:
                explainability_metrics_results.update(metric.calculate(explanations))
            except Exception as e:
                logger.error("Error calculating explainability metric %s: %s", metric.__class__.__name__, e)
        overall_results["explainability_metrics"] = explainability_metrics_results

        # --- Latency Metrics ---
        latency_metrics_results: Dict[str, Any] = {}
        total_runtime_ms = LatencyMetrics.calculate_total_pipeline_runtime(timestamps)

        for metric in self.latency_metrics:
            try:
                if isinstance(metric, ThroughputMetric) and total_runtime_ms is not None:
                    latency_metrics_results.update(metric.calculate(total_runtime_ms, num_initial_candidates))
                elif not isinstance(metric, ThroughputMetric): # All other latency metrics use timestamps
                    latency_metrics_results.update(metric.calculate(timestamps))
            except Exception as e:
                logger.error("Error calculating latency metric %s: %s", metric.__class__.__name__, e)
        overall_results["latency_metrics"] = latency_metrics_results

        # Add config snapshot
        overall_results["configuration_snapshot"] = self.config_manager.get_app_config().model_dump(mode="json")

        # Generate Report
        try:
            report_path = self.report_generator.generate_report(
                overall_results, output_dir / report_name, report_format
            )
            overall_results["report_path"] = str(report_path)
        except Exception as e:
            logger.error("Failed to generate evaluation report: %s", e)
            overall_results["report_generation_error"] = str(e)

        logger.info("Evaluation completed. Results: %s", overall_results)
        return overall_results
