import csv
from pathlib import Path
from typing import Any, Dict, List

from src.interfaces.export_interface import CSVWriter


class CSVGenerator(CSVWriter):
    """Generates CSV files from a list of dictionaries, handling various CSV options."""

    def write_csv(
        self,
        filepath: Path,
        data: List[Dict[str, Any]],
        schema: List[str],
        delimiter: str = ",",
        quotechar: str = ",",
        quoting: int = csv.QUOTE_MINIMAL,
        encoding: str = "utf-8",
        overwrite: bool = False,
    ) -> None:
        """Writes a list of dictionaries to a CSV file.

        Args:
            filepath (Path): The full path to the output CSV file.
            data (List[Dict[str, Any]]): The list of dictionaries to write.
            schema (List[str]): The header row for the CSV.
            delimiter (str): The character used to separate fields.
            quotechar (str): The character used to quote fields containing special characters.
            quoting (int): Controls when quotes should be generated by the writer (e.g., csv.QUOTE_MINIMAL).
            encoding (str): The encoding to use for the output file.
            overwrite (bool): If True, overwrite the file if it already exists. If False, raise an error.

        Raises:
            FileExistsError: If the file exists and overwrite is False.
            ValueError: If the data is empty or schema does not match data keys.
            IOError: For other file writing errors.
        """
        if not data:
            raise ValueError("Data to write cannot be empty.")
        if not schema:
            raise ValueError("Schema (header) cannot be empty.")

        if filepath.exists() and not overwrite:
            raise FileExistsError(
                f"File already exists at {filepath}. Set overwrite=True to proceed."
            )
        filepath.parent.mkdir(parents=True, exist_ok=True)

        try:
            with open(
                filepath, mode="w", newline="", encoding=encoding
            ) as csvfile:
                writer = csv.DictWriter(
                    csvfile,
                    fieldnames=schema,
                    delimiter=delimiter,
                    quotechar=quotechar,
                    quoting=quoting,
                )
                writer.writeheader()
                writer.writerows(data)
        except IOError as e:
            raise IOError(f"Error writing CSV to {filepath}: {e}") from e
        except Exception as e:
            raise Exception(f"An unexpected error occurred during CSV writing: {e}") from e